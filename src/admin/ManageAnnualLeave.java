    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:

        String[][] arrayPending = ann.selectAnnualPending();

        DefaultTableModel model = (DefaultTableModel) this.tblAccess.getModel();

        if (arrayPending != null) {
            for (int i = 0; i < arrayPending.length; i++) //for loop to print the array  
            {
                model.addRow(new Object[]{arrayPending[i][1], arrayPending[i][0], arrayPending[i][2], arrayPending[i][3], arrayPending[i][4], arrayPending[i][5]});
            }
            this.tblAccess.setModel(model);
        }

        String[][] arrayApproved = ann.selectAnnualApproved();

        DefaultTableModel model2 = (DefaultTableModel) this.tblAccess1.getModel();

        if (arrayApproved != null) {
            for (int i = 0; i < arrayApproved.length; i++) //for loop to print the array  
            {
                model2.addRow(new Object[]{arrayApproved[i][1], arrayApproved[i][0], arrayApproved[i][2], arrayApproved[i][3], arrayApproved[i][4], arrayApproved[i][5]});
            }
            this.tblAccess1.setModel(model2);
        }

        String[][] arrayDeclined = ann.selectAnnualDeclined();

        DefaultTableModel model3 = (DefaultTableModel) this.tblAccess4.getModel();

        if (arrayDeclined != null) {
            for (int i = 0; i < arrayDeclined.length; i++) //for loop to print the array  
            {
                model3.addRow(new Object[]{arrayDeclined[i][1], arrayDeclined[i][0], arrayDeclined[i][2], arrayDeclined[i][3], arrayDeclined[i][4], arrayDeclined[i][5] , arrayDeclined[i][6]});
            }
            this.tblAccess4.setModel(model3);
        }

    }//GEN-LAST:event_formWindowOpened

