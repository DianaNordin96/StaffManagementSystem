    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
        try {
            int annualId = Integer.parseInt((String) this.tblAccess.getModel().getValueAt(this.tblAccess.getSelectedRow(), 0));

            if (ann.approve(annualId)) {
                JOptionPane.showMessageDialog(null, "Annual Leave has been approved");
                this.dispose();
                new ManageAnnualLeave(this.UserId, this.accessID, this.adminStatus).setVisible(true);
            }
        } catch (QueryException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnDeclinedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeclinedActionPerformed
        // TODO add your handling code here:
        try {

            JPanel panel = new JPanel(new BorderLayout());
            JTextField reason = new JTextField();
            panel.add(new JLabel("Reason for declined:"), BorderLayout.NORTH);
            panel.add(reason);
//            String reason = JOptionPane.showInputDialog("Reason for declined:");
//            System.out.println(reason);
            JOptionPane.showMessageDialog(null, panel);
            int annualId = Integer.parseInt((String) this.tblAccess.getModel().getValueAt(this.tblAccess.getSelectedRow(), 0));

            if (ann.declined(annualId, reason.getText())) {
                JOptionPane.showMessageDialog(null, "Annual Leave has been declined");
                this.dispose();
                new ManageAnnualLeave(this.UserId, this.accessID, this.adminStatus).setVisible(true);
            }
        } catch (QueryException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }//GEN-LAST:event_btnDeclinedActionPerformed

