    private boolean checkInput() throws EmptyInputException {
        boolean status = false;

        if (this.txtName.getText().equals("")) {
            status = false;
            throw new EmptyInputException();
        } else {
            status = true;
        }
        return status;
    }

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        try {

            if (this.checkInput()) {
                Department dept = new Department();

                dept.setDepartID(dept.getDepartID());
                dept.setDepartName(this.txtName.getText());

                if (dept.addDepartment(this.UserId)) {
                    JOptionPane.showMessageDialog(null, "Added Successfully");
                    this.jTabbedPane1.setSelectedIndex(0);
                    this.setVisible(false);
                    new ManageDepartment(this.UserId, this.accessID,this.adminStatus).setVisible(true);
                }
            }
        } catch (QueryException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (EmptyInputException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

