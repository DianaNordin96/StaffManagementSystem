    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:

        if (!this.txtID.getText().isEmpty()) {
            Department dept = new Department();

            dept.setDepartName(this.txtDeptName.getText());
            if (dept.updateDepartment(this.UserId,Integer.parseInt(this.txtID.getText()))) {
                JOptionPane.showMessageDialog(null, "Data has been updated");

                this.setVisible(false);
                new ManageDepartment(this.UserId, this.accessID,this.adminStatus).setVisible(true);

            }
        } else {
            JOptionPane.showMessageDialog(null, "Cannot update. Must select data from table view.");
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDelteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelteActionPerformed
        // TODO add your handling code here:
        Department dept = new Department();
        String[][] array = dept.selectAllDepartment();

        if (dept.deleteDepartment(Integer.parseInt(array[this.rowSelect][0]))) {
            JOptionPane.showMessageDialog(null, "Record has been deleted");
        }

        this.setVisible(false);
        new ManageDepartment(this.UserId, this.accessID,this.adminStatus).setVisible(true);

    }//GEN-LAST:event_btnDelteActionPerformed

