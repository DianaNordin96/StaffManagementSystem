    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        Department dept = new Department();
        dep = dept.selectAllDepartment();

        DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();

        for (int i = 0; i < dep.length; i++) //for loop to print the array  
        {
            model.addRow(new Object[]{dep[i][0], dep[i][1]});
        }
        this.jTable1.setModel(model);


    }//GEN-LAST:event_formWindowOpened

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int rowSelected = this.jTable1.getSelectedRow();
        this.rowSelect = rowSelected;
        Department dept = new Department();
        String[][] array = dept.selectAllDepartment();
        this.txtID.setText(array[rowSelected][0]);
        this.txtDeptName.setText(array[rowSelected][1]);

        if (rowSelected != -1) {
            this.btnDelte.setEnabled(true);
            this.btnUpdate.setEnabled(true);
        } else {
            this.btnDelte.setEnabled(false);
            this.btnUpdate.setEnabled(false);
        }
    }//GEN-LAST:event_jTable1MouseClicked

