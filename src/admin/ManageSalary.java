    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        DefaultTableModel table = (DefaultTableModel) jTable2.getModel();

        String search = this.txtSearch.getText();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(table);
        jTable2.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(search));
    }//GEN-LAST:event_txtSearchKeyReleased

    private void jBtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateActionPerformed
        // TODO add your handling code here:
        try {

            sal.setEmpId(this.jTxtEmpID.getText());
            sal.setMonth(this.jTxtMonth.getText());
            sal.setSalary(Double.parseDouble(this.jTxtSalary.getText()));
            if (sal.updateSalary()) {
                JOptionPane.showMessageDialog(null, "Update Successfully");
                this.dispose();
                new ManageSalary(this.UserId, this.accessID,this.adminStatus).setVisible(true);
            }
        } catch (QueryException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jBtnUpdateActionPerformed

