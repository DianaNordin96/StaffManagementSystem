    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        int rowSelected = this.jTable2.getSelectedRow();
        this.rowSelect = rowSelected;

        String[][] array = sal.selectAllSalary();

        this.jTxtEmpID.setText(array[rowSelected][0]);
        this.jTxtMonth.setText(array[rowSelected][2]);
        this.jTxtSalary.setText(array[rowSelected][1]);

        if (rowSelected != -1) {
            this.btnEdit.setEnabled(true);
            this.btnDelete.setEnabled(true);
        } else {
            this.btnEdit.setEnabled(false);
            this.btnDelete.setEnabled(false);
        }
    }//GEN-LAST:event_jTable2MouseClicked

