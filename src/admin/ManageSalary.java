    private String getSelectedIndexEmployee() {
        int index = this.ddEmployee.getSelectedIndex();

        Employee emp = new Employee();

        String[][] employee = emp.selectAllEmployee();

        return employee[index][0];

    }

    private String getSelectedIndexMonth() {
        int index = this.ddMonth.getSelectedIndex();

        return this.month[index];

    }

    private boolean checkInput() throws EmptyInputException {
        boolean status = false;

        if (!this.txtSalary.getText().equals("") && this.ddEmployee.getSelectedIndex() != -1 && this.ddMonth.getSelectedIndex() != -1) {
            status = true;
        } else {
            status = false;
            throw new EmptyInputException();
        }
        return status;
    }

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        try {

            if (this.checkInput()) {
                sal.setMonth(this.getSelectedIndexMonth());
                sal.setEmp_id(this.getSelectedIndexEmployee());
                sal.setSalary(Double.parseDouble(this.txtSalary.getText()));
                if (sal.addSalary()) {
                    JOptionPane.showMessageDialog(null, "Salary added succesfully");
                    this.dispose();
                    new ManageSalary(this.UserId, this.accessID,this.adminStatus).setVisible(true);
                }
            }
        } catch (QueryException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (EmptyInputException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

