    private void btnAddAdmin1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddAdmin1ActionPerformed
        // TODO add your handling code here:
        try {
            String[][] employee = emp.selectAllEmployeeNotAdmin();

            adm.setEmp_id(employee[this.rowSelect][0]);

            if (adm.addAdmin()) {
                JOptionPane.showMessageDialog(null, "Admin added successfully");
                this.dispose();
                new ManageAdmin(this.UserId, this.accessID, this.adminStatus).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Admin cannot be added");
            }
        } catch (QueryException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnAddAdmin1ActionPerformed

