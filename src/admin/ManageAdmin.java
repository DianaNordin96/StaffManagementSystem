    public void showDataEmp() {

        String[][] employee = emp.selectAllEmployeeNotAdmin();

        DefaultTableModel model = (DefaultTableModel) jTableEmp.getModel();

        if (employee != null) {
            for (int i = 0; i < employee.length; i++) //for loop to print the array  
            {
                String[] row = new String[employee[i].length];

                for (int j = 0; j < employee[i].length; j++) {
                    row[j] = employee[i][j];
                }
                model.addRow(row);
            }
        }

    }

    public void showDataAdmin() {

        String[][] admin = adm.selectAllAdmin();

        DefaultTableModel model = (DefaultTableModel) this.jTableAdmin.getModel();

        for (int i = 0; i < admin.length; i++) //for loop to print the array  
        {
            model.addRow(new Object[]{admin[i][0], admin[i][1], admin[i][2]});
        }
        this.jTableAdmin.setModel(model);

    }

