    public void showData() {

        Employee emp = new Employee();
        String[][] employee = emp.selectAllEmployee();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        for (int i = 0; i < employee.length; i++) //for loop to print the array  
        {
            String[] row = new String[employee[i].length];

            for (int j = 0; j < employee[i].length; j++) {
                row[j] = employee[i][j];
            }
            model.addRow(row);
        }

    }

