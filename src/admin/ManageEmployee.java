    private boolean checkInput() throws EmptyInputException {
        boolean status = false;

        String id = this.txtID.getText();
        String name = this.txtName.getText();
        String ic = this.txtIC.getText();
        String email = this.txtEmail.getText();
        String dob = this.txtDOB.getText();
        String gender = this.getGender();
        int position = this.getSelectedIndexPosition();
        int department = this.getSelectedIndexDepartment();
        String address = this.txtAdd.getText();
        String contact = this.txtPhone.getText();

        if ((ic != null) && (name != null) && (id != null) && (email != null) && (!this.txtDOB.getText().isEmpty()) && (gender != null) && (position != 0) && (department != 0) && (address != null) && (contact != null)) {
            status = true;
        } else {
            status = false;
            throw new EmptyInputException();
        }
        return status;
    }

    private String getGender() {
        String gender = null;

        if (this.rbFemale.isSelected() || this.rbFemale1.isSelected()) {
            gender = "Female";
        } else if (this.rbMale.isSelected() || this.rbMale1.isSelected()) {
            gender = "Male";
        }

        return gender;
    }

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            if (this.checkInput()) {
                SimpleDateFormat DateFor = new SimpleDateFormat("dd/MM/yyyy");
                Employee obj = new Employee();
                obj.setEmp_id(this.txtID.getText());
                obj.setEmp_name(this.txtName.getText());
                obj.setEmp_ic(this.txtIC.getText());
                obj.setEmp_address(this.txtAdd.getText());
                obj.setEmp_email(this.txtEmail.getText());
                obj.setEmp_contact(this.txtPhone.getText());
                obj.setEmp_gender(this.getGender());
                obj.setEmp_dob(Date.valueOf(this.txtDOB.getText()));
                obj.setEmp_department(this.getSelectedIndexDepartment());
                obj.setEmp_position(this.getSelectedIndexPosition());
                if (obj.addEmployee()) {
                    JOptionPane.showMessageDialog(null, "Added Successfully");
                    this.dispose();
                    new ManageEmployee(this.UserId, this.accessID, this.adminStatus).setVisible(true);
                }
            }

        } catch (QueryException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (EmptyInputException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

