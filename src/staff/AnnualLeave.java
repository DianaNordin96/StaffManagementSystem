    private boolean checkInput() throws EmptyInputException {
        String typeLeave = this.typeLeave.getSelectedItem().toString();
        String reasons = this.txtReasons.getText();
        boolean status = false;

        if ((typeLeave != null) && (this.dateFrom.getDate() != null) && (this.dateTo.getDate() != null) && (reasons != null)) {
            status = true;
        } else {
            status = false;
            throw new EmptyInputException();
        }
        return status;
    }

    private void btnApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyActionPerformed
        // TODO add your handling code here:
        try {
            if (this.checkInput()) {
                SimpleDateFormat DateFor = new SimpleDateFormat("dd/MM/yyyy");

                Annual ann = new Annual();
                ann.setAnnualID(ann.getAnnualID());
                ann.setAnnualType(this.typeLeave.getSelectedItem().toString());
                ann.setAnnualDateFrom(DateFor.format(this.dateFrom.getDate()));
                ann.setAnnualDateTo(DateFor.format(this.dateTo.getDate()));
                ann.setAnnualReason(this.txtReasons.getText());
                ann.setEmpId(this.userId);

                if (ann.addAnnual()) {
                    JOptionPane.showMessageDialog(null, "Added Successfully");
                    this.txtReasons.setText("");
                    this.dispose();
                    new AnnualLeave(this.userId,this.accessID,this.adminStatus).setVisible(true);
                }
            }
        } catch (QueryException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (EmptyInputException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnApplyActionPerformed

