    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:

        Annual ann = new Annual();

        String[][] arrayStaffAnnual = ann.staffAnnual(this.userId);

        DefaultTableModel model3 = (DefaultTableModel) this.tblAccess.getModel();

        if (arrayStaffAnnual != null) {
            for (int i = 0; i < arrayStaffAnnual.length; i++) //for loop to print the array  
            {
                String approval = null;
                if (arrayStaffAnnual[i][4] == null) {
                    approval = "Pending";
                } else if ("1".equals(arrayStaffAnnual[i][4])) {
                    approval = "Approved";
                } else {
                    approval = "Declined";
                }
                model3.addRow(new Object[]{arrayStaffAnnual[i][0], arrayStaffAnnual[i][1], arrayStaffAnnual[i][2], arrayStaffAnnual[i][3], approval , arrayStaffAnnual[i][5]});
            }
            this.tblAccess.setModel(model3);
        }
    }//GEN-LAST:event_formWindowOpened

